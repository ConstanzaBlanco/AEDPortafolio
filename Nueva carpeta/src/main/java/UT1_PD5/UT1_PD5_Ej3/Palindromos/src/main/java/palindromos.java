    private void textPalindromoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_textPalindromoActionPerformed
        String palindrome = this.textPalindromo.getText();
        int len = palindrome.length();
        char[] tempCharArray = new char[len];
        char[] charArray = new char[len];
        
        // put original string in an 
        // array of chars
        for (int i = 0; i < len; i++) {
            tempCharArray[i] = 
                palindrome.charAt(i);
        } 
        
        // reverse array of chars
        for (int j = 0; j < len; j++) {
            charArray[j] =
                tempCharArray[len - 1 - j];
        }
        
        String reversePalindrome =
            new String(charArray);
        this.fraseAlReves.setText(reversePalindrome);
       
        
        if (reversePalindrome.equalsIgnoreCase(palindrome))
        {
            this.esPalindromo.setText("Si");
        }
        else
        {
            this.esPalindromo.setText("No");
        }
    }//GEN-LAST:event_textPalindromoActionPerformed

